                <script type="text/javascript">

                  var save_method; //for save method string
                  var table;
                  $(document).ready(function() {
                    table = $('#table').DataTable({

                      "processing": true, //Feature control the processing indicator.
                      "serverSide": true, //Feature control DataTables' server-side processing mode.

                      // Load data for the table's content from an Ajax source
                      "ajax": {
                          "url": "<?php echo site_url('ciudad/ajax_list')?>",
                          "type": "POST"
                      },

                      //Set column definition initialisation properties.
                      "columnDefs": [
                      {
                        "targets": [ -1 ], //last column
                        "orderable": false, //set not orderable
                      },
                      ],

                    });
                  });

                  function add_ciudad()
                  {
                    save_method = 'add';
                    $('#form')[0].reset(); // reset form on modals
                    $('#modal_form').modal('show'); // show bootstrap modal
                    $('.modal-title').text('Registro de ciudad'); // Set Title to Bootstrap modal title
                  }

                  function edit_ciudad(id)
                  {
                    save_method = 'update';
                    $('#form')[0].reset(); // reset form on modals

                    //Ajax Load data from ajax
                    $.ajax({
                      url : "<?php echo site_url('ciudad/ajax_edit/')?>/" + id,
                      type: "GET",
                      dataType: "JSON",
                      success: function(data)
                      {

                          $('[name="id"]').val(data.id);
                          $('[name="name"]').val(data.detalleCiudad);


                          $('#modal_form').modal('show'); // show bootstrap modal when complete loaded
                          $('.modal-title').text('Editar ciudad'); // Set title to Bootstrap modal title

                      },
                      error: function (jqXHR, textStatus, errorThrown)
                      {
                          alert('Error al cargar los datos');
                      }
                  });
                  }

                  function reload_table()
                  {
                    table.ajax.reload(null,false); //reload datatable ajax
                  }

                  function save()
                  {
                    var url;
                    if(save_method == 'add')
                    {
                        url = "<?php echo site_url('ciudad/ajax_add')?>";
                    }
                    else
                    {
                      url = "<?php echo site_url('ciudad/ajax_update')?>";
                    }

                     // ajax adding data to database
                        $.ajax({
                          url : url,
                          type: "POST",
                          data: $('#form').serialize(),
                          dataType: "JSON",
                          success: function(data)
                          {
                             //if success close modal and reload ajax table
                             $('#modal_form').modal('hide');
                             reload_table();
                          },
                          error: function (jqXHR, textStatus, errorThrown)
                          {
                              alert('!Ops.. Verifique los datos a ingresar..');
                          }
                      });
                  }

                  function delete_ciudad(id)
                  {
                    if(confirm('Realmente Desea Borrar la información?'))
                    {
                      // ajax delete data to database
                        $.ajax({
                          url : "<?php echo site_url('ciudad/ajax_delete')?>/"+id,
                          type: "POST",
                          dataType: "JSON",
                          success: function(data)
                          {
                             //if success reload ajax table
                             $('#modal_form').modal('hide');
                             reload_table();
                          },
                          error: function (jqXHR, textStatus, errorThrown)
                          {
                              alert('Error adding / update data');
                          }
                      });

                    }
                  }

                </script>
